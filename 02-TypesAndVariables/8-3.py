###
# A program that displays your height both in cm and in feet and inches.
#
height_cm = int(input("Enter your height in cm: "))
height_feet = height_cm*0.0328
height_inches = height_cm*0.3937
print(f"Your height in cm: {height_cm}")
print(f"Your height in feet: {height_feet}")
print(f"Your height in inches: {height_inches}")
